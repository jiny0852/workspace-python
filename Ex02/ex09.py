

# while 문

# 6의 배수이자 14의 배수인 가장 적은 정수 찾기

i = 1

while True : 
    
    if i%6==0 and i%14==0 :
        print(i)
        break

    i += 1  # i = i + 1   




# 리스트   : [1,2,3]  순서가있다. index  슬라이싱 [2:5]
# 튜플      : (1,2,3)  리스트의 읽기전용
# 딕션어리: {"a":1, "b":2}  키-값, 순서가없다  키중요   *키-리스트 있어야 전체출력가능   
