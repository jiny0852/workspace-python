# 사용자가 입력한 금액을 기준으로 화폐 단위를 계산하는 프로그램
count = int(input("금액 : "))

# 각 화폐 단위
denominations = [50000, 10000, 5000, 1000, 500, 100, 50, 10, 5, 1]

# 각 단위에 대한 개수를 저장할 딕셔너리
currency_count = {}

# 각 단위에 대해 필요한 개수 계산
for denomination in denominations:
    currency_count[denomination] = count // denomination  # 몫을 구하여 개수를 구함
    count %= denomination  # 나머지를 구하여 다음 단위 계산에 사용

# 결과 출력
for denomination, count in currency_count.items():
    print(f"{denomination}원 : {count}개")


# 코드 설명
# 입력: 사용자가 입력한 금액을 count 변수에 저장합니다.
# 화폐 단위 리스트: denominations 리스트에 각 화폐 단위를 저장합니다.
# 딕셔너리: currency_count 딕셔너리를 사용해 각 화폐 단위의 개수를 저장합니다.
# 계산 반복문: for 루프를 통해 각 화폐 단위에 대해 필요한 개수를 계산합니다.
# count // denomination을 통해 각 화폐 단위의 개수를 구하고,
# count %= denomination을 통해 나머지 금액을 업데이트합니다.
# 결과 출력: 계산된 결과를 출력합니다.

