


age = 33

print(f"나이는: {age}") # 나이: 33
print(f"나이 (최소 너비 5): {age:5d}") # 나이 (최소 너비 5): 33
print(f"나이 (0으로 채우기, 3자리): {age:03d}") # 나이 (0으로 채우기, 3자리): 033


print(f"오른쪽 정렬 (5자리): {age:>5d}") # 오른쪽 정렬 (5자리): 33
print(f"왼쪽 정렬 (5자리): {age:<5d}") # 왼쪽 정렬 (5자리): 33
print(f"가운데 정렬 (5자리): {age:^5d}") # 가운데 정렬 (5자리): 33


