num = 123.456789

#print("숫자: "+ num + " 입니다")
print("숫자: "+ str(num) + " 입니다") # 숫자: 123.456789
print(f"숫자: {num}") # 숫자: 123.456789
print(f"숫자: {num:15f}") 
print(f"숫자: {num:15.2f}") 


print(f"숫자 (소수점 둘째 자리): {num:.2f}") # 숫자 (소수점 둘째 자리): 123.46
print(f"숫자 (소수점 셋째 자리): {num:.3f}") # 숫자 (소수점 셋째 자리): 123.457


print(f"숫자 (오른쪽 정렬, 최소 10자리): {num:>10.2f}") # 숫자 (오른쪽 정렬, 최소 10자리): 123.46
print(f"숫자 (왼쪽 정렬, 최소 10자리): {num:<10.2f}") # 숫자 (왼쪽 정렬, 최소 10자리): 123.46
print(f"숫자 (가운데 정렬, 최소 10자리): {num:^10.2f}") # 숫자 (가운데 정렬, 최소 10자리): 123.46



